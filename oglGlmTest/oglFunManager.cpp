int OglFunManager::initGLFWAndGLAD()
{
    // glfw: initialize and configure
    // ------------------------------
    glfwInit();
    glfwWindowHint(GLFW_CONTEXT_VERSION_MAJOR, 3);
    glfwWindowHint(GLFW_CONTEXT_VERSION_MINOR, 3);
    glfwWindowHint(GLFW_OPENGL_PROFILE, GLFW_OPENGL_CORE_PROFILE);

#ifdef __APPLE__
    glfwWindowHint(GLFW_OPENGL_FORWARD_COMPAT, GL_TRUE);
#endif

    // glfw window creation
    // --------------------
    window = glfwCreateWindow(SCR_WIDTH, SCR_HEIGHT, "LearnOpenGL", NULL, NULL);
    if (window == NULL)
    {
        std::cout << "Failed to create GLFW window" << std::endl;
        glfwTerminate();
        return -1;
    }
    glfwMakeContextCurrent(window);
    glfwSetFramebufferSizeCallback(window, framebuffer_size_callback);

    // glad: load all OpenGL function pointers
    // ---------------------------------------
    if (!gladLoadGLLoader((GLADloadproc)glfwGetProcAddress))
    {
        std::cout << "Failed to initialize GLAD" << std::endl;
        return -1;
    }

    return 0;
}

void OglFunManager::GetShader(ShaderType st)
{
    m_st = st;
    switch (st)
    {
    case enumShader5_1:
    {
        ourShader = Shader("shader/5.1.transform.vs", "shader/5.1.transform.fs");
        break;
    }
    case enumShader6_1:
    {
        ourShader = Shader("shader/6.1.coordinate_systems.vs", "shader/6.1.coordinate_systems.fs");
        break;
    }
    case enumShader6_2:
    {
        // configure global opengl state
        // -----------------------------
        glEnable(GL_DEPTH_TEST);
        ourShader = Shader("shader/6.2.coordinate_systems.vs", "shader/6.2.coordinate_systems.fs");
        break;
    }
    case enumShader6_3:
    {
        // configure global opengl state
        // -----------------------------
        glEnable(GL_DEPTH_TEST);
        ourShader = Shader("shader/6.3.coordinate_systems.vs", "shader/6.3.coordinate_systems.fs");
        break;
    }
    default:
        break;
    }
}

void OglFunManager::SetVerticesTexture(VerticesTYPE vt)
{
    m_vt = vt;
    switch (vt)
    {
    case enum2D:
    {
        SetVAOVBOEBOVerticesTexture();
        break;
    }
    case enum3D:
    case enumMore3D:
    {
        SetVAOVBO3DVerticesTexture();
        break;
    }
    default:
        break;
    }
}

void OglFunManager::SetVAOVBOEBOVerticesTexture()
{
    // set up vertex data (and buffer(s)) and configure vertex attributes
    // ------------------------------------------------------------------
    float vertices[] = {
        // positions          // texture coords
         0.5f,  0.5f, 0.0f,   1.0f, 1.0f, // top right
         0.5f, -0.5f, 0.0f,   1.0f, 0.0f, // bottom right
        -0.5f, -0.5f, 0.0f,   0.0f, 0.0f, // bottom left
        -0.5f,  0.5f, 0.0f,   0.0f, 1.0f  // top left 
    };
    unsigned int indices[] = {
        0, 1, 3, // first triangle
        1, 2, 3  // second triangle
    };
    //unsigned int VBO, VAO, EBO;
    glGenVertexArrays(1, &VAO);
    glGenBuffers(1, &VBO);
    glGenBuffers(1, &EBO);

    glBindVertexArray(VAO);

    glBindBuffer(GL_ARRAY_BUFFER, VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

    glBindBuffer(GL_ELEMENT_ARRAY_BUFFER, EBO);
    glBufferData(GL_ELEMENT_ARRAY_BUFFER, sizeof(indices), indices, GL_STATIC_DRAW);

    // position attribute
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)0);
    glEnableVertexAttribArray(0);
    // texture coord attribute
    glVertexAttribPointer(1, 2, GL_FLOAT, GL_FALSE, 5 * sizeof(float), (void*)(3 * sizeof(float)));
    glEnableVertexAttribArray(1);
}

